# To define a function, you use the def keyword followed by the function name and parentheses.
# If the function requires parameters (inputs), you include them in the parentheses.
# The code block that follows contains the statements that will be executed when the function is called.

# Defining a Function

def greet(name):
    """Prints a greeting message."""
    print(f"Hello, {name}!")

# Calling the Function
greet("Alice")