"""
A Python package is a collection of modules bundled together.
Each module is a file containing Python code, such as functions, classes, or variables, that can be imported and used in your programs.
Packages help organize your code and make it reusable, modular, and easier to manage.

Organization: Packages group related modules together, making your codebase more organized.
Reusability: Once a package is created, it can be reused in different projects.
Sharing: Packages can be shared with others, making it easier to collaborate on projects.
"""

# To use a package in your Python program, you first need to import it.
# You can import an entire package or specific modules from it.

# Importing an entire package
import math

# Using a function from the math package
print(math.sqrt(16))  # Output: 4.0

# Importing a specific function from a package
from math import sqrt

# Using the imported function
print(sqrt(25))  # Output: 5.0

