# Each item in a list has an index, which is a number that denotes the position of the item in the list.
# Python uses zero-based indexing, meaning the first element has an index of 0.
fruits = ["apple", "banana", "cherry"]

# Accessing elements by index
print(fruits[0])  # Output: apple
print(fruits[1])  # Output: banana
print(fruits[2])  # Output: cherry

# Negative indexing
print(fruits[-1])  # Output: cherry (last item)
print(fruits[-2])  # Output: banana (second-to-last item)