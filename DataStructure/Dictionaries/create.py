# Dictionaries in Python are a collection of key-value pairs. Each key is unique, and it maps to a specific value.
# Dictionaries are unordered, changeable, and indexed by keys.

# Example of a dictionary with strings as keys and values
person = {
    "name": "John",
    "age": 30,
    "city": "New York",
    "name" : "Raj",
    "name1" : "Raju"
}

print(person)

