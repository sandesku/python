# A variable is a way to store data in your program.
# Think of it as a container that holds a value, which can be used and modified throughout your code.
age = 13
pi = 3.14
name = "Alice"

# Once a variable is created, you can use it in your code by referring to its name.
print(age)  # Outputs: 13
print(name)  # Outputs: Alice

"""
Variable names must start with a letter or an underscore (_).
They can contain letters, numbers, and underscores but cannot start with a number.
Variable names are case-sensitive (Age and age are different).
"""

print(type(age))
print(type(pi))
print(type(name))